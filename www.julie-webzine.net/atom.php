  <content type="text/html" mode="escaped"> Quel dimanche mes amis ! Le jour du Seigneur ne m'a pas épargnée. Je revenais hier matin de ma boulangerie préférée après avoir craqué pour leurs viennoiseries d'antan, quand un journaliste à moto me pointe de son micro. J'ai rapidement analysé la situation : quartier Notre Dame bouclé + cris de manifestants + habitante du quartier = &quot;Julie, tu vas passer en interview !&quot;. Ca n'a pas loupé. Le motard en question lance un bonjour rapide avant que je rattrappe au vol sa question bateau à propos du baptême de la Place Jean-Paul II. Je ne me rends même pas compte que son magnéto tourne. En 3 minutes, il semble avoir ce qu'il veut et s'engouffre dans la foule, laissant son cheval mécanique au bas de chez moi.&lt;br /&gt;

