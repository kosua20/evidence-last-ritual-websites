			<div class="contenuArticle">
  <p>
    Bienvenue&nbsp;!
  </p>
  <p>
    Afin de répondre à une demande récurrente de mes anciens étudiants, j’ai finalement sacrifié à la mode du blog en novembre 2004. Depuis cette date, plus ou moins régulièrement, j’y note certains
    de mes coups de cœur concernant l’actualité d’un sujet qui me passionne depuis plus de cinquante ans&nbsp;: l’Histoire en général et le Moyen Âge en particulier.
  </p>
  <p>
    Je vous invite à parcourir la page d’aide pour découvrir toutes les facettes de ce blog, et je vous encourage vivement à commenter mes articles, à signer le livre d’or ou à m’écrire directement
    pour me faire part de vos remarques&nbsp;; soyez assuré(e) qu’elles seront toutes lues attentivement et que j'en tiendrai compte pour l'évolution de ce site.
  </p>
  <p>
    Je vous remercie sincèrement de votre visite et vous souhaite une très bonne lecture…
  </p>

  
